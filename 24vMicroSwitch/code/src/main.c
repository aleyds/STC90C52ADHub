static void __Delay(H_U32 ms)
{
	H_U32 i = 0;
	H_U32 j = 0;
	for(i = ms; i> 0; i--)
	{
		for(j = 110; j > 0; j--)
		{
			_nop_();
		}
	}
}

//单片机系统初始化
static void __SystemInit(void)
{
		//外部中断0 配置
		IT0 = 0; //外部中断低电平触发  IT0 = 1 外部中断0下降沿触发
		EX0 = 1; //打开外部中断0  EX0=0;关闭外部中断0
	 //外部中断1 配置
		IT1 = 0; //外部中断低电平触发  IT1 = 1 外部中断1下降沿触发
		EX1 = 1; //打开外部中断1  EX1=0;关闭外部中断1
	
		EA = 1;	//总中断开关
}

//static void 

void main()
{
		__SystemInit();
		_ADCInit();
}